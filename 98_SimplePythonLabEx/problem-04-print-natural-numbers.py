# Problem 4: Print the first 10 natural numbers using a while loop

"""
Explanation: This problem introduces a simple while loop.
Logic: Use a counter variable and increment it in each iteration.
Algorithm:
1. Initialize counter to 1
2. While counter is less than or equal to 10:
   a. Print the counter
   b. Increment the counter
"""

counter = 1
while counter <= 10:
    print(counter, end=" ")
    counter += 1
print()  # New line
